#!/bin/bash
#  _      __     ____                      
# | | /| / /__ _/ / /__  ___ ____  ___ ____
# | |/ |/ / _ `/ / / _ \/ _ `/ _ \/ -_) __/
# |__/|__/\_,_/_/_/ .__/\_,_/ .__/\__/_/   
#                /_/       /_/             
# -----------------------------------------------------
# Set wallpaper
# -----------------------------------------------------

defaultwallpaper="$HOME/.config/wallpapers/sundown-over-water.jpg"

# -----------------------------------------------------
# Load wallpaper
# -----------------------------------------------------
waypaper --wallpaper $defaultwallpaper

# -----------------------------------------------------
# Execute pywal
# -----------------------------------------------------
wal -q -i "$defaultwallpaper"
source "$HOME/.cache/wal/colors.sh"

# -----------------------------------------------------
# Reload Waybar
# -----------------------------------------------------

killall -SIGUSR2 waybar

# Update SwayNC
# -----------------------------------------------------
# sleep 0.1
swaync-client -rs
